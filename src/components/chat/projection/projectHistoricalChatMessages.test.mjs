import test from 'node:test';
import assert from 'node:assert/strict';

import {
  mergeHistoricalChatMessages,
  projectHistoricalChatMessages,
} from './projectHistoricalChatMessages.ts';

test('projectHistoricalChatMessages projects canonical user, assistant, thinking, error and compact messages for legacy pane', () => {
  const projected = projectHistoricalChatMessages([
    {
      id: 'u1',
      sessionId: 'sess-1',
      role: 'user',
      text: '继续执行',
      timestamp: '2026-04-23T10:00:00.000Z',
      kind: 'text',
    },
    {
      id: 't1',
      sessionId: 'sess-1',
      role: 'assistant',
      text: '分析中',
      timestamp: '2026-04-23T10:00:01.000Z',
      kind: 'thinking',
    },
    {
      id: 'c1',
      sessionId: 'sess-1',
      role: 'tool',
      text: null,
      timestamp: '2026-04-23T10:00:02.000Z',
      kind: 'compact_boundary',
    },
    {
      id: 'e1',
      sessionId: 'sess-1',
      role: 'tool',
      text: '执行失败',
      timestamp: '2026-04-23T10:00:03.000Z',
      kind: 'error',
    },
    {
      id: 'a1',
      sessionId: 'sess-1',
      role: 'assistant',
      text: '最终回答',
      timestamp: '2026-04-23T10:00:04.000Z',
      kind: 'text',
    },
  ]);

  assert.deepEqual(
    projected.map((message) => [message.id, message.type, message.normalizedKind]),
    [
      ['u1', 'user', 'text'],
      ['t1', 'assistant', 'thinking'],
      ['c1', 'assistant', 'compact_boundary'],
      ['e1', 'error', 'error'],
      ['a1', 'assistant', 'text'],
    ],
  );
});

