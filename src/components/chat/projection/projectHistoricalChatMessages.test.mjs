test('projectHistoricalChatMessages projects historical user images as real image data when base64 is available', () => {
  const projected = projectHistoricalChatMessages([
    {
      id: 'u-image',
      sessionId: 'sess-image',
      role: 'user',
      text: '看这张图',
      timestamp: '2026-04-26T03:00:00.000Z',
      kind: 'text',
      content: [
        { type: 'text', text: '看这张图' },
        {
          type: 'image',
          source: {
            type: 'base64',
            media_type: 'image/png',
            data: 'ZmFrZS1wbmctZGF0YQ==',
          },
        },
      ],
    },
  ]);

  assert.equal(projected.length, 1);
  assert.equal(projected[0].type, 'user');
  assert.equal(projected[0].content, '看这张图');
  assert.deepEqual(projected[0].images, [
    {
      data: 'data:image/png;base64,ZmFrZS1wbmctZGF0YQ==',
      name: 'image-1.png',
      mimeType: 'image/png',
    },
  ]);
});

