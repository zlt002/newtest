test('projectHistoricalChatMessages hides raw command protocol messages from history', () => {
  const projected = projectHistoricalChatMessages([
    {
      id: 'u1',
      sessionId: 'sess-1',
      role: 'user',
      text: '<command-name>/clear</command-name>\n<command-message>clear</command-message>',
      timestamp: '2026-04-23T10:00:00.000Z',
      kind: 'text',
    },
    {
      id: 'u2',
      sessionId: 'sess-1',
      role: 'user',
      text: '<local-command-caveat>Caveat: The messages below were generated by the user while running local commands.</local-command-caveat>',
      timestamp: '2026-04-23T10:00:01.000Z',
      kind: 'text',
    },
    {
      id: 'a1',
      sessionId: 'sess-1',
      role: 'assistant',
      text: '正常回答',
      timestamp: '2026-04-23T10:00:02.000Z',
      kind: 'text',
    },
  ]);

  assert.deepEqual(
    projected.map((message) => [message.id, message.type, message.content]),
    [
      ['u1', 'user', '/clear'],
      ['a1', 'assistant', '正常回答'],
    ],
  );
});

